-- =============================================================================
-- 15. cpt_code — CPT code reference table for RVU tracking
--
-- Small reference table (~200 rows expected). BIGINT identity PK matches
-- the clinic/provider pattern for low-volume lookup tables.
--
-- Seeded with 12 common outpatient E/M + EKG codes (CY2024 approx values).
-- PE and MP RVU values are rounded / approximate — refer to the official
-- CMS PPRRVU file for production-accurate figures.
--
-- Gold-layer target (via appointment_cpt):
--   SELECT provider_id,
--          date_trunc('month', service_date) AS month,
--          SUM(work_rvu_snapshot * units)   AS total_wrvu,
--          SUM(total_rvu_snapshot * units)  AS total_rvu,
--          SUM(expected_amount)             AS expected_revenue
--   FROM vital_fold.appointment_cpt
--   GROUP BY provider_id, month;
-- =============================================================================
CREATE TABLE vital_fold.cpt_code (
    cpt_code_id         BIGINT         PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY (CACHE 1),
    code                VARCHAR(5)     NOT NULL,
    short_description   VARCHAR(255)   NOT NULL,
    category            VARCHAR(50)    NOT NULL,
    work_rvu            DECIMAL(7, 3)  NOT NULL,
    pe_rvu_nonfacility  DECIMAL(7, 3)  NOT NULL,
    pe_rvu_facility     DECIMAL(7, 3)  NOT NULL,
    mp_rvu              DECIMAL(7, 3)  NOT NULL,
    global_days         SMALLINT,
    effective_year      SMALLINT       NOT NULL,
    is_active           BOOLEAN        NOT NULL DEFAULT TRUE,
    UNIQUE (code, effective_year)
);

CREATE INDEX ASYNC IF NOT EXISTS idx_cpt_code_code
    ON vital_fold.cpt_code (code);
CREATE INDEX ASYNC IF NOT EXISTS idx_cpt_code_category
    ON vital_fold.cpt_code (category);

