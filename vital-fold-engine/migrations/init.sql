-- =============================================================================
-- 16. appointment_cpt — line-item fact table. 1+ rows per appointment.
--
-- The grain for gold-layer wRVU rollups. RVU snapshot columns preserve the
-- value at time of service so historical reports stay stable when CMS updates
-- annual RVU values.
--
-- ID strategy:
--   UUID PK (high-volume, 1+ rows per appointment)
--   UUID FK to appointment  (matches existing appointment_id type)
--   BIGINT FK to cpt_code / provider / clinic (matches identity columns)
-- =============================================================================
CREATE TABLE vital_fold.appointment_cpt (
    appointment_cpt_id  UUID            PRIMARY KEY DEFAULT gen_random_uuid(),
    appointment_id      UUID            NOT NULL,
    cpt_code_id         BIGINT          NOT NULL,
    provider_id         BIGINT          NOT NULL,
    clinic_id           BIGINT          NOT NULL,
    service_date        DATE            NOT NULL,
    units               SMALLINT        NOT NULL DEFAULT 1,
    modifier_1          VARCHAR(2),
    modifier_2          VARCHAR(2),
    work_rvu_snapshot   DECIMAL(7, 3)   NOT NULL,
    pe_rvu_snapshot     DECIMAL(7, 3)   NOT NULL,
    mp_rvu_snapshot     DECIMAL(7, 3)   NOT NULL,
    total_rvu_snapshot  DECIMAL(8, 3)   NOT NULL,
    conversion_factor   DECIMAL(8, 4)   NOT NULL,
    expected_amount     DECIMAL(10, 2),
    creation_time       TIMESTAMP       NOT NULL
);

CREATE INDEX ASYNC IF NOT EXISTS idx_appointment_cpt_appointment
    ON vital_fold.appointment_cpt (appointment_id);
CREATE INDEX ASYNC IF NOT EXISTS idx_appointment_cpt_provider_date
    ON vital_fold.appointment_cpt (provider_id, service_date);
CREATE INDEX ASYNC IF NOT EXISTS idx_appointment_cpt_clinic_date
    ON vital_fold.appointment_cpt (clinic_id, service_date);
CREATE INDEX ASYNC IF NOT EXISTS idx_appointment_cpt_cpt_code
    ON vital_fold.appointment_cpt (cpt_code_id);
